#include "../libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	len_big;

	i = 0;
	if (little[0] == 0)
		return ((char *)big);
	len_big = 0;
	if (big)
		len_big = ft_strlen(big);
	while (i < len && i <= len_big)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j] != '\0' && i + j < len)
			j++;
		if (little[j] == '\0')
			return ((char *)big + i);
		i++;
	}
	return (NULL);
}
