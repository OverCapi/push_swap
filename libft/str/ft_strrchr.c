#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last_pos;

	last_pos = NULL;
	c = (unsigned char)c;
	while (*s != '\0')
	{
		if (*s == c)
			last_pos = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (last_pos);
}
